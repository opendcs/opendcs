package decodes.cwms.algo;

public class TestRow {
    final double elevStart;
    final double elevEnd;
    final double areaStart;
    final double areaEnd;
    final double storStart;
    final double storEnd;

    TestRow(double elevStart, double elevEnd, double areaStart, double areaEnd, double storStart, double storEnd) {
        this.elevStart = elevStart;
        this.elevEnd = elevEnd;
        this.areaStart = areaStart;
        this.areaEnd = areaEnd;
        this.storStart = storStart;
        this.storEnd = storEnd;
    }
}
