package decodes.drgsinfogui;

/**
 * Constant tags for storing DRGS Receiver Identification 
 * information in XML File.
 */
public class DrgsIdentTags
{
	/** Name of the top tag element */
	public static final String drgsIdent = "drgsident";
	/** Name of tag element for every entry in the xml*/
	public static final String ident = "ident";
	/** code tag element */
	public static final String code = "code";
	/** description tag element */
	public static final String description = "description";
	/** location tag element */
	public static final String location = "location";
	/** contact tag element */
	public static final String contact = "contact";
	/** email tag element */
	public static final String email = "email";
	/** phone tag element */
	public static final String phone = "phone";
}
